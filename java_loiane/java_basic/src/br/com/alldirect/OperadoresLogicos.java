package br.com.alldirect;

public class OperadoresLogicos {

	public static void main(String[] args) {
		
		boolean verdade = true;
		boolean falso = false;
		
		System.out.println(verdade && falso);
		System.out.println(verdade || falso);
		System.out.println(verdade ^ falso);
		System.out.println(!verdade && falso);

		
		

	}

}
